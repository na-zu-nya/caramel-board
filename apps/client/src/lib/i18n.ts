export const translations: Record<AppLanguage, Translations> = { en, ja };

export const useT = (): Translations => {
  const language = useLanguage();
  return translations[language];
};

export function isScratchLike(value?: CollectionLike | PinLike | null): boolean {
  if (!value) return false;
  const collection =
    'collection' in value && value.collection ? value.collection : (value as CollectionLike);
  return (
    collection.type === 'SCRATCH' ||
    collection.kind === 'scratch' ||
    collection.filterConfig?.kind === 'scratch' ||
    String(collection.name || '')
      .trim()
      .toLowerCase() === 'scratch'
  );
}

export function getMediaTypeLabel(t: Translations, mediaType?: string | null): string {
  if (mediaType === 'image') return t.pins.images;
  if (mediaType === 'comic') return t.pins.comics;
  if (mediaType === 'video') return t.pins.videos;
  return mediaType ?? '';
}

export function getDefaultPinDisplayName(t: Translations, pin: PinLike): string {
  if (pin.type === 'MEDIA_TYPE' && pin.mediaType) {
    return getMediaTypeLabel(t, pin.mediaType);
  }
  if (pin.type === 'OVERVIEW') return t.pins.overview;
  if (pin.type === 'FAVORITES') return t.pins.favorites;
  if (pin.type === 'LIKES') return t.pins.likes;
  if (pin.type === 'COLLECTION' && isScratchLike(pin)) {
    return t.pins.scratch;
  }
  if (pin.type === 'COLLECTION') {
    return pin.collection?.name || pin.name || t.common.untitled;
  }
  return pin.name || t.common.untitled;
}
